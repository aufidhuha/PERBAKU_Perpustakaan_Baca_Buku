    void auto_id(){
        
        try {
            String sql = "SELECT * FROM pinjam ORDER BY id_pinjam DESC";
            Connection cnVar = koneksi.getKoneksi();
            Statement stVar = cnVar.createStatement();
            ResultSet rsVar = stVar.executeQuery(sql);
            
            if (rsVar.next()) {
                String idPinjam = rsVar.getString("id_pinjam").substring(4);
                String valueIDPInjam = "" + (Integer.parseInt(idPinjam) + 1);
                String nol = "";
                
                if (valueIDPInjam.length() == 1) {
                    nol = "00000";
                } else if (valueIDPInjam.length() == 2) {
                    nol = "0000";
                } else if (valueIDPInjam.length() == 3) {
                    nol = "000";
                } else if (valueIDPInjam.length() == 4) {
                    nol = "00";
                } else if (valueIDPInjam.length() == 5) {
                    nol = "0";
                } else if (valueIDPInjam.length() == 6) {
                    nol = "";
                }     
                
                txtIDPinjam.setText("KDPJ" + nol + valueIDPInjam);
            } else {
                txtIDPinjam.setText("KDPJ000001");
            }
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, sQLException.getMessage());
        }
        
    }

