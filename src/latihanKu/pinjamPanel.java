    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        String idPinjam = txtIDPinjam.getText();
        String namaPeminjam = txtNamaPeminjam.getText();
        String tanggalPinjam = txtTanggalPinjam.getText();
        Date JDate = (Date) JDateTanggalKembali.getDate();
        int total = Integer.parseInt(txtTotalBuku.getText());
        
        if (idPinjam.isBlank() || namaPeminjam.isBlank() || tanggalPinjam.isBlank() || JDateTanggalKembali.getDate() == null || txtTotalBuku.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Harap mengisi data secara keseluruhan", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String tanggalKembali = new SimpleDateFormat("yyyy-MM-dd").format(JDate);
        
        if (tablePinjam.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(null, "Harap memilih buku untuk Peminjaman", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            Connection cnVar = koneksi.getKoneksi();
            String sqlCheck = "SELECT * FROM pinjam WHERE id_pinjam = ?";
            
            PreparedStatement psVar = cnVar.prepareStatement(sqlCheck);
            psVar.setString(1, idPinjam);
            ResultSet rsVar = psVar.executeQuery();
            
            if (rsVar.next()) {
                JOptionPane.showMessageDialog(null, "Peminjaman sudah dilakukan, harap Muat Ulang untuk membuat Peminjaman baru", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            } else {
                
                String sqlPinjam = "INSERT INTO pinjam VALUES (?, ?, ?, ?, ?)";
                PreparedStatement psVarPinjam = cnVar.prepareStatement(sqlPinjam);
                
                psVarPinjam.setString(1, idPinjam);
                psVarPinjam.setString(2, namaPeminjam);
                psVarPinjam.setString(3, tanggalPinjam);
                psVarPinjam.setString(4, tanggalKembali);
                psVarPinjam.setInt(5, total);
                psVarPinjam.execute();
                psVarPinjam.close();
                
                String sqlDetailPinjam = "INSERT INTO detail_pinjam VALUES (?, ?, ?, ?)";
                PreparedStatement psVarDetailPinjam = cnVar.prepareStatement(sqlDetailPinjam);
                int jumlahData = tablePinjam.getRowCount();
                
                for (int i = 0; i < jumlahData; i++) {
                  
                    psVarDetailPinjam.setString(1, idPinjam);
                    psVarDetailPinjam.setString(2, tablePinjam.getValueAt(i, 0).toString());
                    psVarDetailPinjam.setString(3, tanggalPinjam);
                    psVarDetailPinjam.setString(4, tanggalKembali);
                    psVarDetailPinjam.execute();
                    
                }
                psVarDetailPinjam.close();
                
                JOptionPane.showMessageDialog(null, "Peminjaman berhasil", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error : " + sQLException.getMessage());
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, exception.getMessage());
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

